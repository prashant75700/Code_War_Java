package Clumsy.D3.Loops;

public class Countdown {
    public static void main(String[] args) {
        
        System.out.println("\nCountdown Start from 10: ");

        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
        System.out.println("Blast aborted, yuuoohho :) \n");
    }
}

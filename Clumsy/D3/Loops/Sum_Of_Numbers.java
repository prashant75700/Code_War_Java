package Clumsy.D3.Loops;

public class Sum_Of_Numbers {
    public static void main(String[] args) {
        
        int sum = 0 ;
        System.out.println("");

        for (int i = 1; i <= 100; i++) {
            sum += i;
        }
        System.out.println("Sum of 0 to 100 = " + sum + "\n");
    }
}

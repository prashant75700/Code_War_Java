package Clumsy.D3.Array;

public class For_Each_loop {
    
    public static void main(String[] args) {
        
        String [] fruits = { "Apple", "Mango", "Grapes"};
         
        System.out.println("\nAll Fruits: ");
        for(String fruit : fruits) {
            System.out.println(fruit);
        }

    }
}

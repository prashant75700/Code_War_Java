public class Ex_AgeCal {
    public static void main(String[] args) {
        int myage = 26;
        int tempage = myage;

        tempage = myage + 5;
        System.out.println("Your age will be " + tempage + " after 5 years later.");

        tempage = myage + 7;
        System.out.println("Your age will be " + tempage + " after 7 years later.");

        tempage = myage + 16;
        System.out.println("Your age will be "+ tempage + " after 16 years later.");

        tempage = myage - 8;
        System.out.println("You were " + tempage + " years old Eight years ago.");
    }
}

public class StringExploration {
    
    public static void main(String[] args) {
        //here we learn how to combine strings 

        String firstname = "Sora";

        String lastname = "Elfine";

        String fullname = firstname + " " + lastname;

        System.out.println("Your Fullname is : " + fullname);

        //string with Numbers: 

        String phoneNo = "3483675312";
        String name = "Jamuna";

        System.out.println("Contact Details: \n" + name +":\n" + phoneNo);

    }
}
